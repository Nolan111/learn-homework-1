import logging
import ephem
import datetime
import settings

from telegram.ext import Updater, CommandHandler, MessageHandler, Filters

logging.basicConfig(format='%(name)s - %(levelname)s - %(message)s',
                    level=logging.INFO,
                    filename='bot.log')


PROXY = {
    'proxy_url': settings.PROXY_URL,
    'urllib3_proxy_kwargs': {
        'username': settings.PROXY_USERNAME,
        'password': settings.PROXY_PASSWORD
    }
}


def greet_user(update, context):
    text = 'Вызван /start'
    print(text)
    update.message.reply_text(text)


def talk_to_me(update, context):
    user_text = update.message.text
    print(user_text)
    update.message.reply_text(user_text)

def planet_ (update, context):
    try:
        user_planet = update.message.text.split()[-1].capitalize()
        
        date = datetime.datetime.now().strftime("%Y/%m/%d")
        planet_ = getattr(ephem, user_planet)(date)
        planet_.compute(date)
        planet_ = ephem.constellation(planet_)
        target_planet = f"Планета {user_planet} сегодня в созвездии {planet_[1]}!" 

        update.message.reply_text(target_planet)


    except AttributeError:
        message = 'Я не знаю что это за планета'
        print(message)
        update.message.reply_text('Я не знаю что это за планета')

def main():
    mybot = Updater(settings.API_KEY, request_kwargs=PROXY, use_context=True)

    dp = mybot.dispatcher
    dp.add_handler(CommandHandler("planet", planet_))
    dp.add_handler(CommandHandler("start", greet_user))
    dp.add_handler(MessageHandler(Filters.text, talk_to_me))

    mybot.start_polling()
    mybot.idle()


if __name__ == "__main__":
    main()
